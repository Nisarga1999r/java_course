class Unary4a     
{  
public static void main(String args[])  
{  
int i = 1212;  
i--;    
--i;                   
System.out.println(i);    
System.out.println(--i);    
System.out.println(i--);    
System.out.println(i--);  
System.out.println(i);
System.out.println();

int a = 2021;
System.out.println(a);
System.out.println(a--); 
System.out.println(--a); 
System.out.println(a);
System.out.println();

int b = 221;
System.out.println(b--);    
System.out.println(b--); 
System.out.println(b++);
System.out.println(--b); 
}  
}  